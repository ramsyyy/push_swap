#include "push_swap.h"

void	sasb(t_list *a)
{
	int	tmp;

	if (a->prev == NULL)
	{
		tmp = a->nb;
		a->nb = a->next->nb;
		a->next->nb = tmp;
	}
	back_to_start(&a);
}
